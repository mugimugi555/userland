#!/bin/bash

echo "=========================================="
echo "🚀 X サーバー（Xorg）インストールスクリプト"
echo "=========================================="

# ======================================================================
# 必要なパッケージをインストール
# ======================================================================
echo "📌 Xorg をインストール中..."
sudo apt update
sudo apt install -y xserver-xorg xinit x11-xserver-utils xauth x11-utils x11-xkb-utils mesa-utils

# ======================================================================
# `.Xauthority` を適切に作成
# ======================================================================
echo "📌 `.Xauthority` を作成..."
touch ~/.Xauthority
xauth generate :0 . trusted

# ======================================================================
# X サーバーのテスト
# ======================================================================
echo "📌 Xorg の動作確認..."
Xorg -version

echo "=========================================="
echo "🎉 Xorg のインストールが完了しました！"
echo "=========================================="
