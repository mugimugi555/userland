#!/bin/bash

# ======================================================================================================================
# システムアップデート
# ======================================================================================================================
sudo apt update ;
sudo apt upgrade -y ;

# ======================================================================================================================
# 必須ツール（基本的な CUI ユーティリティ）
# ======================================================================================================================
sudo apt install -y curl wget unzip zip tar xz-utils htop neofetch screen tmux tree ;

# curl: HTTPリクエストを送る
# wget: ファイルのダウンロード
# unzip, zip, tar, xz-utils: 圧縮・解凍ツール
# htop: システムモニタ（プロセス管理）
# neofetch: システム情報の表示
# screen: 仮想ターミナルマルチプレクサ
# tmux: 高機能なターミナルマルチプレクサ
# tree: ファイル構造をツリー状に表示

# ======================================================================================================================
# 開発ツール（ビルド環境など）
# ======================================================================================================================
sudo apt install -y git make build-essential clang cmake gdb ;

# git: ソース管理システム
# make: ビルドツール
# build-essential: C/C++ 開発環境
# clang: C/C++ コンパイラ
# cmake: ビルド設定ツール
# gdb: デバッガ

# ======================================================================================================================
# ネットワーク管理ツール
# ======================================================================================================================
sudo apt install -y net-tools dnsutils iproute2 iputils-ping nmap socat tcpdump whois ;

# net-tools: ifconfig, netstat などのネットワークツール
# dnsutils: nslookup, dig などの DNS 解析ツール
# iproute2: ip コマンド（ifconfig の代替）
# iputils-ping: ping コマンド
# nmap: ネットワークスキャナ
# socat: 通信のプロキシツール（nc の強化版）
# tcpdump: ネットワークパケットキャプチャ
# whois: ドメイン情報の取得

# ======================================================================================================================
# ファイル管理ツール
# ======================================================================================================================
sudo apt install -y ranger ncdu lsof fd-find ripgrep ;

# ranger: CUI ファイルマネージャ
# ncdu: ディスク使用量を CUI で表示
# lsof: 開いているファイルのリスト表示
# fd-find: 高速な find コマンド
# ripgrep: 高速な grep（Rust 製）

# ======================================================================================================================
# システム監視・管理ツール
# ======================================================================================================================
sudo apt install -y btop iftop iotop sysstat psmisc ;

# btop: 高機能なシステムモニタ（htop の進化版）
# iftop: ネットワークトラフィックモニタ
# iotop: ディスク I/O モニタ
# sysstat: sar コマンドなどの統計情報収集ツール
# psmisc: killall, fuser, pstree などのプロセス管理ツール

# ======================================================================================================================
# テキスト処理・検索ツール
# ======================================================================================================================
sudo apt install -y jq fzf bat exa silversearcher-ag ;

# jq: JSON データの処理ツール
# fzf: インタラクティブなファイル検索ツール
# bat: 高機能な cat コマンド（シンタックスハイライト付き）
# exa: 高機能な ls コマンド（カラー対応）
# silversearcher-ag: 超高速な grep 代替（ag コマンド）

# ======================================================================================================================
# CLI での快適操作向けツール
# ======================================================================================================================
sudo apt install -y zsh fish tldr moreutils ;

# zsh: 高機能なシェル（bash の上位互換）
# fish: 使いやすい代替シェル（オートサジェスト付き）
# tldr: コマンドの簡単な説明を表示
# moreutils: sponge, pee, vidir など便利なツール群

# ======================================================================================================================
# 設定完了メッセージ
# ======================================================================================================================
echo "==========================="
echo "CUI ユーティリティのセットアップが完了しました"
echo "UserLAnd を再起動してください"
echo "==========================="
